package com.libo.entity;

import java.sql.Date;

public class Box {
	public Box() {
	}
	public Box(int id, String title, String content, int senduserid,
			Date senddate, String sendstate, int receiveuserid,
			Date receivedate, int receivestate) {
		this.id = id;
		this.title = title;
		this.content = content;
		this.senduserid = senduserid;
		this.senddate = senddate;
		this.sendstate = sendstate;
		this.receiveuserid = receiveuserid;
		this.receivedate = receivedate;
		this.receivestate = receivestate;
	}
	private int id;
	private String title;
	private String content;
	private int senduserid;
	private Date senddate;
	private String sendstate;
	private int receiveuserid;
	private Date receivedate;
	private int receivestate;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return the content
	 */
	public String getContent() {
		return content;
	}
	/**
	 * @param content the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}
	/**
	 * @return the senduserid
	 */
	public int getSenduserid() {
		return senduserid;
	}
	/**
	 * @param senduserid the senduserid to set
	 */
	public void setSenduserid(int senduserid) {
		this.senduserid = senduserid;
	}
	/**
	 * @return the senddate
	 */
	public Date getSenddate() {
		return senddate;
	}
	/**
	 * @param senddate the senddate to set
	 */
	public void setSenddate(Date senddate) {
		this.senddate = senddate;
	}
	/**
	 * @return the sendstate
	 */
	public String getSendstate() {
		return sendstate;
	}
	/**
	 * @param sendstate the sendstate to set
	 */
	public void setSendstate(String sendstate) {
		this.sendstate = sendstate;
	}
	/**
	 * @return the receiveuserid
	 */
	public int getReceiveuserid() {
		return receiveuserid;
	}
	/**
	 * @param receiveuserid the receiveuserid to set
	 */
	public void setReceiveuserid(int receiveuserid) {
		this.receiveuserid = receiveuserid;
	}
	/**
	 * @return the receivedate
	 */
	public Date getReceivedate() {
		return receivedate;
	}
	/**
	 * @param receivedate the receivedate to set
	 */
	public void setReceivedate(Date receivedate) {
		this.receivedate = receivedate;
	}
	/**
	 * @return the receivestate
	 */
	public int getReceivestate() {
		return receivestate;
	}
	/**
	 * @param receivestate the receivestate to set
	 */
	public void setReceivestate(int receivestate) {
		this.receivestate = receivestate;
	}
}
